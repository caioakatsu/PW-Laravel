    <h1>Framework </h1>
    <h2> O que é um framework? </h2>
    <a> Frameworks são estruturas compostas por um conjunto de códigos genéricos que permite o desenvolvimento de sistemas e aplicações. Um framework funciona como uma espécie de template ou modelo que, quando utilizado, oferece certos artifícios e elementos estruturais básicos para a criação de alguma aplicação ou software.</a>

    <h2> Vantagens </h2>
        <ol>
            <li><a>Aumento da produtividade </a> </li>
            <li><a>Códigos sem erros </a> </li>
            <li><a>Fácil manutenção </a> </li>
            <li><a>Maior segurança </a> </li>
            <li><a>Projeto padronizado </a> </li>
        </ol>

